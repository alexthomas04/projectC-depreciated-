package ablilities;


/**
 * The Class Ablility.
 */
public abstract class Ablility {
	
	/** The level. */
	private int level=0;
	
	/** The name. */
	private String name="";

	/**
	 * Instantiates a new ablility.
	 */
	public Ablility() {
		
	}
	
	/**
	 * Gets the type.
	 *
	 * @return the type
	 */
	abstract String getType();
	
	

}
